#While loop
print("Printing 1 -> 10")
a = 1
#Printing front
while(a<=10):
    print(a)
    a = a+1

#Printing reverse
print("Printing 10 -> 1")
a = 10
while(a>=1):
    print(a)
    a = a-1
#Print even numbers in 1 to 10
print("Printing even numbers between 1 and 10")
a = 1
while(a<=10):
    if(a%2 == 0):
        print(a)
    a = a+1

