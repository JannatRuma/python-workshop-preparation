#introduction to math module
import math
a = 1.23
b = math.floor(a)
c = math.ceil(a)
print("Number: ", a)
print("Floor: ", b)
print("Ceil: ", c)
print("Square root of 16: ", math.sqrt(16))
print("Mod for floor: ", math.fmod(5,2.5))
print("Gcd of 12, 60 is: ", math.gcd(12,60))
print("Log 2 base of 4: ", math.log2(4))
print("Log 10 base of 100: ", math.log10(100))
print("Log n base 9 base 3: ", math.log(9,3))
print("Sin(45): ", math.sin(45))
print("Radian of 180: ", math.radians(180))