#Assignment operators
print("Assignment Operators")
a = 5
print("a = {}".format(a))
a += 5
print("a += 5 , Result: ", a)
a -= 1
print("a -= 1 , Result: ", a)
a *= 2
print("a *= 2 , Result: ", a)
a /= 3
print("a /= 3 , Result: ", a)
a //= 3
print("a //= 3, Result: ", a)
a **= 4
print("a **= 4, Result: ", a)
a %= 2
print("a %= 2, Result: ", a)

