# Logical Operators
print("Logical Operators")
print("'and' operator")
print("\t2 == 2 and 3 == 3 Result: ", (2==2 and 3 ==3))
print("\t2 == 2 and 3 == 2 Result: ", (2==2 and 3 ==2))
'''
For and operator, 
    if all are true then the output is true
    if even a single one is false than the output is false
'''

print("'or' operator")
print("\t2 == 2 or 3 == 3 Result: ", (2==2 or 3 ==3))
print("\t2 == 2 or 3 == 2 Result: ", (2==2 or 3 ==2))
'''
For or operator
    if all are false then the output is false
    if even a single one is true then the output is true
'''

print("'not' operator")
print("\tnot(2==2) Result: ", (not(2==2)))
print("\tnot(2==3) Result: ", not(2==3))
'''
The not operator is just like the ! in C 
    it outputs the flip of the condition
'''
