#Conditional Operators or Comparison Operators
a = 5
b = 2
print("Inputs: ", a, b)
print("{} == {} Result: {}".format(a,b, a==b))
print("{} != {} Result: {}".format(a,b, a!=b))
print("{} > {} Result: {}".format(a,b, a >b))
print("{} < {} Result: {}".format(a,b, a<b))
print("{} >= {} Result: {}".format(a,b, a>=b))
print("{} <= {} Result: {}".format(a,b, a<=b))